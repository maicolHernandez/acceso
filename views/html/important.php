                  Bootstrap
                  <div class="mt-2 mr-2">Bootstrap está constituido por una serie de archivos CSS y JavaScript responsables de asignar características específicas a los elementos de la página.

Hay un archivo principal llamado bootstrap.css, que contiene una definición para todos los estilos utilizados. Básicamente, la estructura del framework se compone de dos directorios:

<ul>
    <li>
    css: contiene los archivos necesarios para la estilización de los elementos y una alternativa al tema original;
    </li>
    <li>
    js: contiene la parte posterior del archivo bootstrap.js (original y minificado), responsable de la ejecución de aplicaciones de estilo que requieren manipulación interactiva.</div>
    </li>

</ul>
